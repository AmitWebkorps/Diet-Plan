package com.healthymeal.modal;

public class Plans {
   String planName;
   String planPrice;
   String planDays;
   String weightFrom;
   String weightTo;
public String getPlanName() {
	return planName;
}
public void setPlanName(String planName) {
	this.planName = planName;
}
public String getPlanPrice() {
	return planPrice;
}
public void setPlanPrice(String planPrice) {
	this.planPrice = planPrice;
}
public String getPlanDays() {
	return planDays;
}
public void setPlanDays(String planDays) {
	this.planDays = planDays;
}
public String getWeightFrom() {
	return weightFrom;
}
public void setWeightFrom(String weightFrom) {
	this.weightFrom = weightFrom;
}
public String getWeightTo() {
	return weightTo;
}
public void setWeightTo(String weightTo) {
	this.weightTo = weightTo;
}
   
}
